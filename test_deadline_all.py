def test_all_deadlines():
    parser = SmartParser()
    passed = 0
    failed = 0
    failed_cases = []
    
    for item in test_data:
        # 从期望结果中提取年份作为publish_time
        expected = item["expected"]
        publish_time = ""
        if "2025" in expected:
            publish_time = "2025-01-01"
        elif "2026" in expected:
            publish_time = "2026-01-01"
        result = parser.parse_deadline(item["text"], publish_time)
        
        # 将空字符串视为与"（空）"等价
        if (result == expected) or (result == "" and expected == "（空）"):
            passed += 1
            print(f"OK ID {item['id']}: {result}")
        else:
            failed += 1
            failed_cases.append(item['id'])
            print(f"FAIL ID {item['id']}: 期望 '{expected}', 实际 '{result}'")
    
    print(f"\n测试结果: {passed} 个通过, {failed} 个失败")
    if failed_cases:
        print(f"失败的ID: {failed_cases}")
    
    return failed_cases

if __name__ == "__main__":
    test_all_deadlines()
